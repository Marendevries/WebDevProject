    <h5 class="card-title">Spelregels Poker Texas Hold em</h5>
    <p class="card-text">De blinds:<br>
        Voordat we echt beginnen te spelen is de eerste speler links van de dealer verplicht om een vooraf afgesproken bedrag in te zetten (small blind), waarna de speler links van deze small blind op zijn beurt de big blind zet, die in de meeste gevallen twee keer zo hoog is als de small blind.
        <br>De hoogte hangt af van de gespeelde limiet.
        <br>Bij online poker gaat het allemaal automatisch.<br>
        <br>Eerste inzetronde:<br>
        De speler aan de linkerkant van de big blind is als eerste aan de beurt.<br>
        Hij heeft drie opties: call, raise of fold.<br> Bij call zet de speler het zelfde bedrag in als de big blind, bij raise zet deze pokerspeler een extra bedrag in. <br>
        Bij fold is deze ronde voor hem ten einde, je speelt niet meer mee om de pot, maar het kost hem ook geen inzet.<br> Bijkomende optie is re-raise, een extra verhoging na een raise.
        
        <br><br>Nogmaals de drie opties:<br>
        Fold = Je kaarten zijn te slecht om te spelen. Je gooit ze weg. (mucken)<br>
        Call = Je gaat mee met de inzet van de big blind en legt hetzelfde aantal fiches in.<br>
        Raise = Je verhoogt de inzet en legt dus meer in dan de big blind.<br>

        <br>De Flop en de 2e inzetronde:<br>
        Er komen 3 kaarten op tafel die voor iedereen zichtbaar zijn.<br>
        Deze drie kaarten vormen de flop en elke speler gebruikt ze om samen met zâ€™n handkaarten een combinatie van vijf kaarten te maken.<br>
        Behalve call, raise of re-raise kun je ook checken.<br>
        Je mag gratis doorspelen tot de volgende beurt. Als er een inzet voor je is gedaan, kun je callen voor dat bedrag of het bedrag verhogen (raise).<br>
        
        <br>De Turn en de 3e inzetronde:<br>
        Na de tweede inzetronde, wordt de volgende kaart open gelegd.<br>
        Dit is de turn kaart.<br> Deze kaart is weer een aanvulling op de handkaarten van de spelers om een pokerhand van vijf kaarten te maken.<br>
        Spelers kunnen weer checken, betten, callen, raisen en natuurlijk folden.<br>
        
        <br>De River en de laatste inzetronde:<br>
        Na de derde inzetronde wordt de vijfde en laatste gemeenschappelijke kaart open gelegd.<br>
        Dit is de river kaart.<br> Nu volgt de laatste inzetronde. <br>Deze gaat vooraf aan de showdown.<br>
        Dat is het moment dat iedereen die nog in het spel zit de kaarten moeten openleggen.<br>
        Winnaar is degene met de hoogste kaart, hij krijgt de pot, bij gelijke kaarten wordt er gedeeld.<br>
        
        <br>Split pot:<br>
        Wanneer er twee dezelfde winnende handen zijn, is het split pot.<br>
        Kan de pot niet precies worden verdeeld en is er sprake van een overblijvende chip, dan gaat deze naar de speler die de laatste inzet of verhoging heeft gedaan.<br>
        Is dat ook onbeslist dan gaat de chip naar de speler links van de dealer.</p>
